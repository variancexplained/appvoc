import inspect
from datetime import datetime
import pytest
import logging

import pandas as pd
import numpy as np

# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.eda
class TestEDA:  # pragma: no cover
    # ============================================================================================ #
    def test_overview(self, eda, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        no_cache_start = datetime.now()
        eda.overview
        no_cache_end = datetime.now()
        duration = (no_cache_end - no_cache_start).total_seconds()

        with_cache_end = datetime.now()
        with_duration = (with_cache_end - no_cache_end).total_seconds()
        assert with_duration < duration

        logger.debug(f"\n{eda.overview}")
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)

