import inspect
from datetime import datetime
import pytest
import logging
from time import sleep
import numpy as np

from appvoc.infrastructure.web.throttle import AThrottle


# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.delay
class TestDelay:  # pragma: no cover
    # ============================================================================================ #
    def test_delay(self, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        throttle = AThrottle(
            burnin_period=5,
            burnin_reset=20,
            burnin_rate=5,
            burnin_threshold_factor=2,
            rolling_window_size=5,
            cooldown_factor=2,
            cooldown_phase=10,
            rate=10,
            verbose=5,
        )
        for i in range(100):
            throttle.start()
            if i < 30:
                latency = np.random.rand()
            elif i < 60:
                latency = 2 * np.random.rand()
            else:
                latency = np.random.rand()
            sleep(int(latency))
            throttle.stop()
            throttle.delay()
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)

